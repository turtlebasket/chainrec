        <div class="text-3xl font-semibold">About</div>
        <br>
        <div class="text-lg">Many legal, professional and interpersonal situations require timestamped evidence, for example:</div><br>
        <ul class="text-lg list-disc">
            <li>An individual's will and testament must be signed and dated at the time of its creation 
                with public witnesses in order to be considered legally valid.</li>
            <li>In countries adhering to the
            <a class="underline" href="https://en.wikipedia.org/wiki/Berne_Convention" target="_blank">Berne Convention</a>, 
            a creator's copyright is valid at the time his or her work is "fixed".</li>
        </ul>
        <br>
        <div class="text-lg">
            But in an environment as dynamic and highly-connected as the modern internet, how can one retain proof of having created a 
            work or transcript before a certain date? As it turns out, blockchains offer a simple solution to this proof-of-existence 
            problem. A public, immutable ledger containing timestamped hashes (uniquely generated IDs) of digital creations offers a form of 
            long-term, tamper-resistant proof that a piece of digital content irrefutably existed at a certain point in time. 
            ChainRec is a simple application that offers users this guarantee.
        </div>
        <br>
        <div class="text-lg">
            As some might point out, however, putting one's name & other personal information on a public blockchain 
            may seem a tad excessive. This is why ChainRec is built for for and deployed on the 
            <a class="underline" href="https://scrt.network" target="_blank">Secret Network</a>, 
            a blockchain that allows ChainRec to store all information immutably, while allowing for some data fields
            to only be viewable by authorized user addresses. Records' titles, content hashes and timestamps can 
            be freely queried, but the personal information contained within is only accessible when authorized by its creator.
        </div> 
