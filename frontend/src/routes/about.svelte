        <div class="text-lg">
            What constitutes a valid copyright claim? In countries adhering to the
            <a class="underline" href="https://en.wikipedia.org/wiki/Berne_Convention" target="_blank">Berne Convention</a>, 
            a copyright claim is valid once the creator's work is "fixed". But, in an environment as dynamic and highly-connected
            as the modern internet, how can an author retain proof of having created a work by a certain date? 
            As it turns out, blockchains offer a simple solution to this problem. A public, immutable ledger containing 
            timestamped hashes (uniquely generated IDs) of digital creations offers a form of long-term, tamper-resistant 
            proof that a piece of digital content irrefutably existed at a certain point in time. This guarantee is what 
            ChainRec offers.
        </div>
        <br>
        <div class="text-lg">
            As some might point out, however, putting one's name & legal information on a public blockchain purely for
            copyright protection may seem a tad excessive. This is why ChainRec is built for for and deployed on the 
            <a class="underline" href="https://scrt.network" target="_blank">Secret Network</a>, 
            a blockchain that allows any information to be stored immutably, but optionally allows for some data fields
            to only be viewable by authorized user addresses. Records' titles, content hashes and timestamps can 
            be freely queried, but the personal information contained within is only accessible when authorized by its author.
        </div> 
